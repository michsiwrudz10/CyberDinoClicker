Object.entries(TRANSLATION_OVERRIDES).forEach(([language, entries]) => {
  TRANSLATIONS[language] = {
    ...(TRANSLATIONS[language] || {}),
    ...entries
  };
});


export function normalizeLanguage(code) {
  if (!code) return "en";
  const lowered = String(code).toLowerCase();
  if (TRANSLATIONS[lowered]) return lowered;
  const short = lowered.split(/[-_]/)[0];
  if (TRANSLATIONS[short]) return short;
  return "en";
}

function getInitialLanguage() {
  try {
    const stored = typeof window !== "undefined" ? window.localStorage.getItem(STORAGE_KEY) : "";
    if (stored) return normalizeLanguage(stored);
  } catch {}

  try {
    if (typeof navigator !== "undefined") {
      return normalizeLanguage(navigator.language || navigator.languages?.[0]);
    }
  } catch {}

  return "en";
}

function interpolate(template, values = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => {
    if (Object.prototype.hasOwnProperty.call(values, key)) {
      return values[key];
    }
    return `{${key}}`;
  });
}

function isBrokenTranslation(value) {
  const text = String(value || "").trim();
  if (!text) return false;
  const questionMarks = (text.match(/\?/g) || []).length;
    const mojibakePatterns = ["\u0102", "\u00C2", "\u00CB", "\uFFFD", "\u00E2\u20AC", "\u00E2\u201A\u00AC", "\u00C4\u201A"];
  return mojibakePatterns.some((pattern) => text.includes(pattern)) || questionMarks >= Math.max(2, Math.ceil(text.length * 0.35));
}

function translateWithLanguage(language, key, values = {}, fallback = "") {
  const candidate = TRANSLATIONS[language]?.[key];
  const template = candidate && !isBrokenTranslation(candidate) ? candidate : (fallback ?? key);
  return interpolate(template, values);
}

export function translateMessage(key, values = {}, fallback = "") {
  return translateWithLanguage(activeLanguage, key, values, fallback);
}

const I18nContext = createContext({
  language: "en",
  setLanguage: () => {},
  languages: LANGUAGE_OPTIONS,
  t: (key, values, fallback) => translateWithLanguage(activeLanguage, key, values, fallback)
});

export function I18nProvider({ children }) {
  const [language, setLanguageState] = useState(getInitialLanguage);
  activeLanguage = language;

  const setLanguage = (nextLanguage) => {
    const normalized = normalizeLanguage(nextLanguage);
    setLanguageState(normalized);
    activeLanguage = normalized;
    try {
      window.localStorage.setItem(STORAGE_KEY, normalized);
    } catch {}
  };

  const value = useMemo(() => ({
    language,
    setLanguage,
    languages: LANGUAGE_OPTIONS,
    t: (key, values = {}, fallback = "") => translateWithLanguage(language, key, values, fallback)
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}





